                <center><h1><strong>KABUPATEN BEKASI</strong></h1>
                <br>
                <img src="gambar/kabupaten bekasi.jpg" width="100" height="100"></center>
                <br><br>
                <p>Kabupaten Bekasi adalah sebuah kabupaten di Provinsi Jawa Barat, Indonesia. Ibukotanya adalah Cikarang. Kabupaten ini berada tepat di sebelah timur Jakarta, berbatasan dengan Kota Bekasi dan Provinsi DKI Jakarta di barat, Laut Jawa di barat dan utara, Kabupaten Karawang di timur, serta Kabupaten Bogor di selatan. Kabupaten Bekasi terdiri atas 23 kecamatan, yang dibagi lagi atas sejumlah desa dan kelurahan. </p>
                <p>Sejarah terbentuknya Kabupaten Bekasi dimulai dengan dibentuknya   &quot;Panitia Amanat Rakyat Bekasi&quot; yang dipelopori R. Supardi, M. Hasibuan,   KH. Noer Alie, Namin, Aminudin dan Marzuki Urmaini, yang menentang   keberadaan RIS- Pasundan dan menuntut berdirinya kembali Negara Kesatuan   RI. Selanjutnya diadakan Rapat Raksasa di Alun-alun Bekasi yang   dihadiri oleh sekitar 40.000 orang rakyat Bekasi pada tanggal 17   Pebruari 1950.</p>
                <p>  Menyampaikan tuntutan Rakyat Bekasi yang berbunyi : satu:   Penyerahan kekuasaan Pemerintah Federal kepada Republik Indonesia. dua:   Pengembalian seluruh Jawa Barat kepada Negara Republik Indonesia. tiga:   Tidak mengakui lagi adanya pemerintahan di daerah Bekasi, selain   Pemerintahan Republik Indonesia. empat: Menuntut kepada Pemerintah agar   llama Kabupaten Jatinegara diganti menjadi Kabupaten Bekasi. </p>
                <p>Upaya para   pemimpin Panitia Amanat Rakyat Bekasi untuk memperoleh dukungan dari   berbagai pihak terus dilakukan. Diantaranya mendekati para pemimpin   Masjumi, tokoh militer (Mayor Lukas Kustaryo dan Moh. Moefreini Mukmin)   di Jakarta. Pengajuan usul dilakukan tiga kali antarambulan Pebruari   sampai dengan bulan Juni 1950 hingga akhimya setelah dibicarakan dengan   DPR RIS, dan Mohammad Hatta menyetujuim penggantian nama &quot;Kabupaten   Jatinegara&quot; menjadi &quot;KabupatenBekasi &quot;. </p>
                <p>Persetujuan pembentukan   Kabupaten Bekasi semakin kuat setelah dikeluarkannya Undang-undang No.   14 Tahun 1950. Kabupaten Bekasi secara resmi dibentuk dan ditetapkan   tanggal 15 Agustus 1950 sebagai Hari Jadi Kabupaten Bekasi. Selanjutnya   pada tanggal 2 April 1960 Pusat Pemda Bekasi semula dipusatkan di   Jatinegara (sekarang Markas Kodim 0505 Jayakarta, Jakarta) dipindahkan   ke gedung baru Mustika Pura Kantor Pemda Bekasi yang terletak di Bekasi   Kaum JI. Jr. H. Juanda.</p>
