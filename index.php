                  <center><h1><strong>KOTA BEKASI</strong></h1>
                  <br>
                  <img src="gambar/kota bekasi.jpg" width="100" height="100"></center>
                  <br><br>
                  <p>Kota ini merupakan  bagian dari megapolitan Jabodetabek dan menjadi kota satelit dengan jumlah  penduduk terbanyak di Indonesia. Saat ini Kota Bekasi berkembang menjadi tempat  tinggal kaum urban dan sentra industri. Kota bekasi juga dijuluki sebagai Kota  Patriot dan Kota Pejuang Sejarah ditandai dengan aksi unjuk rasa sekitar 40.000  rakyat Bekasi pada tanggal 17 Februari 1950 di alun-alun Bekasi. Hadir pada  acara tersebutBapak Mu’min sebagai Residen Militer Daerah V. </p>
                  <p>Inti dari unjuk rasa tersebut adalah penyampaian pernyataan  sikap sebagai berikut: Rakyat bekasi mengajukan usul kepada Pemerintah Pusat agar  Kabupaten Jatinegara diubah menjadi Kabupaten Bekasi. Rakyat Bekasi tetap  berdiri di belakang Pemerintah Negara Kesatuan Republik Indonesia. Berdasarkan UU Nomor 14 Tahun 1950 terbentuklah  Kabupaten Bekasi, dengan wilayah terdiri dari 4 kewedanaan, 13 kecamatan  (termasuk Kecamatan Cibarusah) dan 95 desa. Angka-angka tersebut secara  simbolis diungkapkan dalam lambang Kabupaten Bekasi dengan motto  &quot;SWATANTRA WIBAWA MUKTI&quot;.</p>
                  <p>Pada tahun 1960 kantor Kabupaten Bekasi berpindah dari  Jatinegara ke kota Bekasi (jl. H Juanda). Kemudian pada tahun 1982, saat Bupati  dijabat oleh Bapak H. Abdul Fatah Gedung Perkantoran Pemda Kabupaten Bekasi  kembali dipindahkan ke Jl. A. Yani No.1 Bekasi. Pasalnya perkembangan Kecamatan  Bekasi menuntut dimekarkannya Kecamatan Bekasi menjadi Kota Administratif  Bekasi yang terdiri atas 4 kecamatan berdasarkan Peraturan Pemerintah Nomor 48  Tahun 1981, yaitu Kecamatan Bekasi Timur, bekasi Selatan, Bekasi Barat dan  Bekasi Utara, yang seluruhnya menjadi 18 kelurahan dan 8 desa.</p>
                  <p>Peresmian Kota Administratif Bekasi dilakukan oleh Menteri  Dalam Negeri pada tanggal 20 April 1982, dengan walikota pertama dijabat oleh  Bapak H. Soedjono (1982 – 1988). Tahun 1988 Walikota Bekasi dijabat oleh Bapak  Drs. Andi Sukardi hingga tahun 1991 (1988 - 1991, kemudian diganti oleh Bapak  Drs. H. Khailani AR hingga tahun (1991 – 1997) </p>
                  <p>
                  Pada Perkembangannya Kota Administratif Bekasi terus  bergerak dengan cepat. Hal ini ditandai dengan pertumbuhan penduduk yang cukup  tinggi dan roda perekonomian yang semakin bergairah. Sehingga status Kotif.  Bekasi pun kembali di tingkatkan menjadi Kotamadya (sekarang &quot;Kota&quot;) melalui Undang-undang Nomor 9 Tahun 1996 Menjabat Walikotamadya Kepala Daerah Tingkat II Bekasi saat itu adalah Bapak Drs. H. Khailani AR, selama satu tahun (1997-1998).</p>
